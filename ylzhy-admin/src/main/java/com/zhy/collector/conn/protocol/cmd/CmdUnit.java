package com.zhy.collector.conn.protocol.cmd;

/**
 * @author : zengqiang
 * @version V1.0
 * @Project: ylxny-zhy
 * @Package com.zhy.collector.conn.protocol.cmd
 * @Description: 命令执行单元 用于并行多数据的操作
 * @date Date : 2018年09月06日 15:27
 */
public class CmdUnit{
    //命令
    private String cmd;
    //命令标识
    private String cmd_code;
    //命令类型
    private String cmd_type;
    //初始位置 modbus寄存器位置
    private int start_position;
    //基础命令数量
    private int base_cmd_count;
    //采集频率
    private int frequency;
    //操作类型
    private int oper_type;

    public String getCmd() {
        return cmd;
    }

    public void setCmd(String cmd) {
        this.cmd = cmd;
    }

    public String getCmd_code() {
        return cmd_code;
    }

    public void setCmd_code(String cmd_code) {
        this.cmd_code = cmd_code;
    }

    public String getCmd_type() {
        return cmd_type;
    }

    public void setCmd_type(String cmd_type) {
        this.cmd_type = cmd_type;
    }

    public int getStart_position() {
        return start_position;
    }

    public void setStart_position(int start_position) {
        this.start_position = start_position;
    }

    public int getBase_cmd_count() {
        return base_cmd_count;
    }

    public void setBase_cmd_count(int base_cmd_count) {
        this.base_cmd_count = base_cmd_count;
    }

    public int getFrequency() {
        return frequency;
    }

    public void setFrequency(int frequency) {
        this.frequency = frequency;
    }

    public int getOper_type() {
        return oper_type;
    }

    public void setOper_type(int oper_type) {
        this.oper_type = oper_type;
    }
}
